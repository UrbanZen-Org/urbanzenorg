						The Urban Zen Foundation (UZF) has been extremely active since Hurricane Matthew decimated and paralyzed much of Haiti’s southern region on October 4, 2016.
   					 	<br /><br />
						Immediately following the hurricane, UZF, collaborated with its Haitian partner on the ground PRODEV, a leading educator in Haiti, along with a team of connectors; Maryse Kedar, Caroline Sada, Paula Coles, and Natacha Cassis who managed a strategy and army of volunteers to deliver critical aid in the form of <strong>17,500 emergency kits</strong>, each providing for a family of 4.  These kits were sourced through donations coordinated by Maison Handal and delivered via UN trucks, by foot, and by barge to the population cut off from mainland Haiti, including Ile A Vache, Jeremie, Port-Salut, Dame Marie, along with over 15 other communities in the South, like Tiburon which had not received aid prior to our delivery.  The kits included food, water, medical supplies, hygiene kits, and water purification tablets to sustain life delivered to over <strong>70,000 children and their families.</strong>.
						<br /><br />
						As UZF supports Haiti’s efforts to rebuild, the next step is to get the region’s <strong>7,000 school children</strong> back in the classroom. Providing support in the areas of education and healthcare, with redevelopment experts, this initiative is focused on PRODEV’s 27 schools and communities, which will need new books, school supplies, school uniforms, school furniture, and psychological support after the hurricane.
